public interface NumberOp {

    /**
     * 加法
     * @param number1
     * @param number2
     * @return
     */
    int add(int number1, int number2);

    /**
     * 减法 Subtract
     * @param number1
     * @param number2
     * @return
     */
    int Subtract(int number1, int number2);

    /**
     * 乘法
     * @param number1
     * @param number2
     * @return
     */
    int mult(int number1, int number2);

    /**
     * 除法
     * @param number1
     * @param number2
     * @return
     */
    int divide(int number1, int number2);



}
